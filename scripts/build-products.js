#!/usr/bin/env node
/**
 * build-products.js — generate scripts/products-catalog.js from data/products.json
 *
 * Pipeline:
 *   data/products.json  →  (this script)  →  scripts/products-catalog.js
 *                                          (loaded by index.html, auto-merges
 *                                           into window.P and auto-injects
 *                                           product cards into the right grid)
 *
 * Usage:
 *   node scripts/build-products.js
 *   npm run build:products
 *
 * Behavior:
 *   - Reads data/products.json (array of product objects, see schema)
 *   - Skips entries with active: false
 *   - Generates an IIFE that: (a) merges new entries into window.P, (b) builds
 *     the same product-card HTML index.html already uses, (c) inserts each
 *     card into the grid that matches the product's sector on DOMContentLoaded
 *   - Also generates a MATCH_CATALOG supplement that concatenates onto the
 *     existing one on load (used by AI Partner Match)
 *
 * Additive, not destructive: legacy inline products in index.html stay exactly
 * as they are — this script only APPENDS. The build output is idempotent, so
 * running it repeatedly produces the same file.
 *
 * Every generated string goes through JSON.stringify so no manual escaping
 * is needed — paste any Korean / quoted content straight into products.json.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'data', 'products.json');
const OUT = path.join(ROOT, 'scripts', 'products-catalog.js');

function load() {
  const raw = fs.readFileSync(SRC, 'utf8');
  const parsed = JSON.parse(raw);
  if (!parsed.products || !Array.isArray(parsed.products)) {
    throw new Error('data/products.json must contain a top-level "products" array');
  }
  return parsed.products;
}

function validate(p) {
  const req = ['id', 'sector', 'model', 'part', 'sub', 'desc', 'specs', 'features'];
  for (const k of req) {
    if (p[k] === undefined || p[k] === null) {
      throw new Error(`Product "${p.id || '(no id)'}" missing required field: ${k}`);
    }
  }
  if (!/^[a-z0-9-]+$/.test(p.id)) {
    throw new Error(`Product id "${p.id}" must match ^[a-z0-9-]+$ (lowercase, digits, hyphens)`);
  }
  if (!Array.isArray(p.specs) || !p.specs.every(s => Array.isArray(s) && s.length === 2)) {
    throw new Error(`Product "${p.id}" specs must be array of [label, value] tuples`);
  }
  if (!Array.isArray(p.features)) {
    throw new Error(`Product "${p.id}" features must be an array of strings`);
  }
}

function toPEntry(p) {
  const entry = {
    model: p.model,
    sub: p.sub,
    part: p.part,
    specs: p.specs,
    features: p.features,
    desc: p.desc
  };
  if (p.img) entry.img = p.img;
  return entry;
}

function toMatchEntry(p) {
  return {
    id: p.id,
    name: `${p.model}${p.sub ? ' — ' + p.sub : ''}`,
    sector: sectorLabel(p.sector),
    tags: p.tags || [],
    scale: p.scale || ['m'],
    desc: p.matchDesc || p.sub || '',
    coming: !!p.coming
  };
}

function sectorLabel(key) {
  return ({
    'k-security':      'K-Security',
    'k-tech':          'K-Tech',
    'k-energy':        'K-Energy',
    'k-bio':           'K-Bio',
    'k-beauty':        'K-Beauty',
    'k-culture-goods': 'K-Culture Goods',
    'k-franchise':     'K-Franchise',
    'k-smart-living':  'K-Smart Living'
  })[key] || key;
}

function sectorGridId(key) {
  return ({
    'k-security': 'productGrid',
    'k-tech':     'ktechGrid',
    'k-energy':   'kenergyGrid',
    'k-bio':      'kbioGrid',
    'k-beauty':   'kbeautyGrid'
    /* Non-active sectors don't have a grid yet — cards for them are held
       back until the sector goes active and gets a grid id. */
  })[key];
}

function main() {
  const products = load().filter(p => p.active !== false);
  products.forEach(validate);

  /* Duplicate-id check across ONLY the new rows (legacy inline ids aren't
     visible to this script; the runtime Object.assign would simply overwrite
     any collision, which is surprisingly fine — user should pick a unique id
     anyway). */
  const seen = new Set();
  for (const p of products) {
    if (seen.has(p.id)) throw new Error(`Duplicate id in products.json: ${p.id}`);
    seen.add(p.id);
  }

  const pMap = {};
  const matchRows = [];
  const cardsBySector = {};
  for (const p of products) {
    pMap[p.id] = toPEntry(p);
    matchRows.push(toMatchEntry(p));
    (cardsBySector[p.sector] = cardsBySector[p.sector] || []).push(p);
  }

  /* Emit output file — an IIFE so no globals leak other than the merges
     onto window.P and window.MATCH_CATALOG. */
  const banner =
    '/* AUTO-GENERATED by scripts/build-products.js — DO NOT EDIT BY HAND.\n' +
    '   Source:  data/products.json\n' +
    '   Command: npm run build:products\n' +
    '   Built:   ' + new Date().toISOString() + ' */\n';

  const cardBuilder = String(function cardHtmlFor(id, p, i) {
    /* Mirrors index.html's inline card markup so the style matches 1:1. */
    const tag  = (p.cardTag || p.sub || '').replace(/"/g, '&quot;');
    const mfg  = (p.maker || 'ERGSN CO., LTD.') + ' <span class="card-verified">\u2713 Verified Partner</span>';
    const miniSpecs = (p.specs || []).slice(0, 4)
      .map(([k, v]) => `<div><p class="spec-lbl">${k}</p><p class="spec-val">${v}</p></div>`)
      .join('');
    const imgBlock = p.img
      ? `<div class="card-image"><picture><source srcset="${p.img.replace(/\.jpg$|\.png$/, '.webp')}" type="image/webp"><img src="${p.img}" alt="ERGSN ${p.model}" loading="lazy"></picture></div>`
      : '';
    const cardHtml =
      `<div class="product-card reveal" style="--i:${i}" tabindex="0" data-model="${id}" onclick="openModal('${id}')" onkeydown="if(event.key==='Enter'&&event.target===this)openModal('${id}')">` +
        imgBlock +
        `<p class="card-tag">${tag}</p>` +
        `<p class="card-model">${p.model}</p>` +
        `<p class="card-part">Part No: ${p.part}</p>` +
        `<p class="card-mfg">${mfg}</p>` +
        `<p class="card-desc">${p.desc.split('. ')[0]}.</p>` +
        `<div class="card-specs">${miniSpecs}</div>` +
        `<div class="card-foot">` +
          `<span class="card-cta">View Full Specs</span>` +
          `<button class="compare-btn" type="button" aria-label="Add ${p.model} to compare" onclick="event.stopPropagation();toggleCompare('${id}',this)">` +
            `<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round"><path d="M6 12h12M12 6v12"/></svg>Compare` +
          `</button>` +
        `</div>` +
      `</div>`;
    return cardHtml;
  });

  /* Emit the entire generated file as a runtime IIFE. Product data is
     serialised via JSON.stringify for safe quoting — no need to hand-escape
     Korean / apostrophe-heavy content. */
  const body = [
    banner,
    '(function () {',
    '  var ADDITIONS = ' + JSON.stringify(pMap, null, 2) + ';',
    '  var MATCH_ADDITIONS = ' + JSON.stringify(matchRows, null, 2) + ';',
    '  var CARDS_BY_SECTOR = ' + JSON.stringify(cardsBySector, null, 2) + ';',
    '  var SECTOR_GRID = {',
    '    "k-security": "productGrid",',
    '    "k-tech":     "ktechGrid",',
    '    "k-energy":   "kenergyGrid",',
    '    "k-bio":      "kbioGrid",',
    '    "k-beauty":   "kbeautyGrid"',
    '  };',
    '',
    '  /* Merge into the lexical `P` binding declared in index.html (const P at',
    '     script scope is shared across classic scripts via the global lexical',
    '     environment, but NOT exposed on window). `typeof` is safe on undeclared',
    '     identifiers, so the guard works on pages without an inline P. */',
    '  try {',
    '    if (typeof P !== "undefined" && P && typeof P === "object") {',
    '      Object.keys(ADDITIONS).forEach(function (id) {',
    '        if (!P[id]) P[id] = ADDITIONS[id];',
    '      });',
    '    }',
    '  } catch (_) {}',
    '  try {',
    '    if (typeof MATCH_CATALOG !== "undefined" && Array.isArray(MATCH_CATALOG)) {',
    '      MATCH_ADDITIONS.forEach(function (m) {',
    '        if (!MATCH_CATALOG.some(function (x) { return x.id === m.id; })) {',
    '          MATCH_CATALOG.push(m);',
    '        }',
    '      });',
    '    }',
    '  } catch (_) {}',
    '',
    '  /* Also expose on window — standalone pages\' chat-core.js reads window.P. */',
    '  if (!window.P) window.P = {};',
    '  Object.keys(ADDITIONS).forEach(function (id) {',
    '    if (!window.P[id]) window.P[id] = ADDITIONS[id];',
    '  });',
    '  if (Array.isArray(window.MATCH_CATALOG)) {',
    '    MATCH_ADDITIONS.forEach(function (m) {',
    '      if (!window.MATCH_CATALOG.some(function (x) { return x.id === m.id; })) {',
    '        window.MATCH_CATALOG.push(m);',
    '      }',
    '    });',
    '  }',
    '',
    '  /* Runtime card builder — mirrors index.html card markup byte-for-byte. */',
    '  var cardHtmlFor = ' + cardBuilder.toString() + ';',
    '',
    '  function injectCards() {',
    '    Object.keys(CARDS_BY_SECTOR).forEach(function (sector) {',
    '      var gridId = SECTOR_GRID[sector];',
    '      if (!gridId) return; /* sector not active — skip for now */',
    '      var grid = document.getElementById(gridId);',
    '      if (!grid) return;',
    '      var existing = Array.prototype.map.call(grid.querySelectorAll(":scope > .product-card"), function (c) { return c.getAttribute("data-model"); });',
    '      CARDS_BY_SECTOR[sector].forEach(function (p, i) {',
    '        if (existing.indexOf(p.id) !== -1) return; /* already rendered inline */',
    '        var tmp = document.createElement("div");',
    '        tmp.innerHTML = cardHtmlFor(p.id, ADDITIONS[p.id], i);',
    '        grid.appendChild(tmp.firstChild);',
    '      });',
    '    });',
    '  }',
    '',
    '  if (document.readyState === "loading") {',
    '    document.addEventListener("DOMContentLoaded", injectCards);',
    '  } else {',
    '    injectCards();',
    '  }',
    '})();',
    ''
  ].join('\n');

  fs.writeFileSync(OUT, body, 'utf8');
  console.log(`✓ Wrote ${OUT} — ${products.length} product(s) emitted`);
}

try {
  main();
} catch (e) {
  console.error('✗ build-products failed:', e.message);
  process.exit(1);
}
